    // TODO à tester
    @Override
    public synchronized void addReference(EcritureComptable pEcritureComptable) {
        // TODO à implémenter
        // Bien se réferer à la JavaDoc de cette méthode !
        /* Le principe :
                1.  Remonter depuis la persitance la dernière valeur de la séquence du journal pour l'année de l'écriture
                    (table sequence_ecriture_comptable)
                2.  * S'il n'y a aucun enregistrement pour le journal pour l'année concernée :
                        1. Utiliser le numéro 1.
                    * Sinon :
                        1. Utiliser la dernière valeur + 1
                3.  Mettre à jour la référence de l'écriture avec la référence calculée (RG_Compta_5)
                4.  Enregistrer (insert/update) la valeur de la séquence en persitance
                    (table sequence_ecriture_comptable)
         */
        
        //On récupère le code et la date de l'ecriture passée en paramètre pour les comparer avec la liste
        //provenant de la base de données
        String date = Integer.toString(pEcritureComptable.getDate().getYear() + 1900);
        String code = pEcritureComptable.getJournal().getCode();
        //Booléen pour savoir si il existe une écriture correspondant à l'année. flag = true si il existe au moins
        //une ecriture pour cette année et flag = false sinon.
        Boolean flag = false;
        
        List<EcritureComptable> lEcrs = new ArrayList<>();
        lEcrs = getDaoProxy().getComptabiliteDao().getListEcritureComptable();
        //L'index correspond au numéro sur 5 chiffres à la fin de la référence
        int index = 0;
        for (EcritureComptable tEcr : lEcrs) {
            String tRef = tEcr.getReference();
            if(tRef != null) {
                //on cherche à avoir la plus grande référence qui existe pour l'année concernée
                String[] tCode;
                String[] tDate;
                tCode = tRef.split("-");
                tDate = tCode[1].split("/");
                String fDate = tDate[0];
                if(fDate.equals(date) && tCode[0].equals(code)) {
                    flag = true;
                    int pIndex = Integer.parseInt(tDate[1]);
                    if (pIndex > index) {
                        index=pIndex;
                    }
                }
            }
        }
        String ref;
        if(flag == false) {
            ref = code + "-" + date + "/" + "00001";
        }else {
            //Il faut que l'index soit écrit sur 5 chiffre or l'int retourné peut être écrit sur moins de chiffres.
           String fIndex = Integer.toString(index +1);
           int i = fIndex.length();
           while(i < 5) {
               fIndex = "0" + fIndex;
               i = fIndex.length();
           }
           ref = code + "-" + date + "/" + fIndex;
        }
        pEcritureComptable.setReference(ref);
        getDaoProxy().getComptabiliteDao().insertEcritureComptable(pEcritureComptable);
    }

